          <h3 class="materials-title-s">关于LMI的理念</h3>
          <p class="materials-title-s">1.	成功</p>
          <p>什么是成功？当你想到成功时脑海中会浮现出哪些词语？</p>
          <p>请在笔记本中写下这些词语。</p>
          <p>我们对成功的定义是：逐步的实现、有价值的、预先设定的、个人的目标。</p>
          <p>成功是<b>逐步的</b>：它是一个持续而充满变化的旅程，而不是一个终点。“逐步的”意味着当你设定目标并为之奋斗时你就成功了。成功降临于你采取行动去达成目标的时刻，而非目标达成的时刻。</p>
          <p>成功意味着<b>实现</b>。这表示你确实正在达成目标，你不仅仅只是设定目标而已，而是你正在实现着目标。</p>
          <p>成功表示你的目标是<b>有价值的</b>。也许你会问，“对谁有价值？”首先，对你自己有价值。你的目标可能对别人也有价值，但不能违反任何法律规定。</p>
          <p>你的目标必须是<b>预先设定的</b>。简单来说，就是你最好先思考并设立目标，知道一年、三年、五年后你会到达哪里。自己的目标最好由自己决定，而不是由他人来为你做决定。</p>
          <p>你的目标是<b>个人的</b>。它们必须是你自己的目标。当你为别人的目标奋斗时，想必不会像为自己的目标奋斗那样动力十足。</p>
          <p>最后，你设定的<b>目标</b>必须是具体的、可衡量的、可达成的、现实的和实质的，并且要涉及到你生活的各个领域：事业、家庭、理财、心智、身体、社交和精神。有时人们认识不到拥有目标有多重要，特别是书面目标的重要性。</p>
          <p>所以成功的定义是：逐步的实现、有价值的、预先设定的、个人的目标。</p>
          <p class="materials-title-s">这是实现目标的过程，是目标导向的行动！</p>
          <p>如果你有目标，并为之奋斗，你就是成功的。</p>
          <p>&nbsp;</p>
          <p class="materials-title-s">2. 激励</p>
          <p>什么是激励？我们常常被什么激励？</p>
          <p>为了理解激励（MOTIVATION）的含义，让我们把这个词一分为二。激励MOTIVATION由二部分组成，MOTIVE和ACTION。MOTIVE是理由、目的或目标，ACTION与运动或动作是同义词。因此激励是朝向目标运动，即激励是目标导向的行动。这与我们对成功的定义内核是相同的。实质上，激励与成功是同义词，它们携手并进。</p>
          <p>如果成功和激励真的如此简单，那么就应该有更多的人成功，更多的人被激励，但事实上却并非如此。我们现在来看一下大多数情景下人们所使用的激励模式以及为何收效甚微。</p>
          <p>首先是建立在强迫和惩罚基础上的<b>恐惧式激励</b>。在这种模式下，人之所以行动是因为担心自己不行动会招致坏的结果。同样为了避免招致惩罚，人们也会想出许多应对的策略，从而逐渐培养出对恐惧式激励的免疫力。例如：学会敷衍了事，做事做到不挨老板批评的程度即可，或者只要老板不点头就什么也不做。</p>
          <p>恐惧式激励是外在和暂时的。它不会让人成长、发展和运用他们的潜能。</p>
          <p>第二种模式是建立在奖赏基础上的<b>诱因式激励</b>。常见的诱因包括奖金、旅行、奖章或其它为表彰工作而设立的奖赏。它们有用吗？当然，在一定程度上有。但如果今年的奖励跟去年的一样，会发生什么呢？</p>
          <p>诱因式激励跟恐惧式激励存在同样的缺陷，即：是外在的和暂时的。</p>
          <p>那么什么样的激励是内在的、长效的？</p>
          <p>我们所关注的是建立在改变基础上的<b>态度式激励</b>。态度式激励的有效源于它是内在的，这意味着你是自我激励的，不需要依赖他人来激励你。</p>
          <p>&nbsp;</p>
          <p class="materials-title-s">3. 改变态度</p>
          <p>现在我们来看一下态度式激励将如何真正地改变你的人生，使你对生活的掌控力超乎想象。</p>
          <p>注意观察我们就能看到，几乎我们每天做的每一件事都是出于习惯。</p>
          <p>我们学习做事，它们就成为了我们的习惯。小时候，我们学会了走路、说话、骑车、系鞋带，养成了数百种习惯。如今这些都成了“无意识的”或“反射性的”活动。</p>
          <p>谈到习惯，我们常常会想到坏习惯，这是因为我们倾向于关注坏习惯，但事实是你的绝大多数习惯都是让你富有成效的好习惯。</p>
          <p>俗话说，你先养成习惯，然后由习惯成就你或毁灭你。既然习惯如此重要，我们就一起来看看习惯是如何形成的，又可以如何改变。</p>
          <p>你的思维方式和做事方式最初是如何形成的？</p>
          <p>生活中你获得成功的程度取决于你所创造的成果。成果越大，成功越大。</p>
          <p>你所创造的成果是你言行举止或处事方式的结果。</p>
          <p>你的言行举止反映出你的<b>态度</b>，即你的习惯性思考模式。实际上，“思维的习惯”是对态度的最佳描述。</p>
