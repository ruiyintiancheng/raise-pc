          <p>你的态度或思维习惯是制约的结果。</p>
          <p>而制约只是习惯的另一个说法。</p>
          <p>受一些人的影响，如家人、朋友、老师、老板等，我们形成了某种特定的思维和行为方式。</p>
          <p>最后因着一些思想或行为在较长时间内的不断重复，形成了我们的制约。我们把这些重复称为间隔重复。因为它们不断重复，且中间有间隔。</p>
          <p>现在到了最激动人心的部分，想象一下：如果是这个过程塑造出了今天的我们，那意味着我们可以使用同一过程来塑造我们的未来。换句话说…</p>
          <p>如果我们有意识地让强有力的思想或行为<b>间隔重复</b>，就能改变我们的制约程序，继而我们的思维习惯即态度也会随之改变。</p>
          <p>态度上的改变会带来行为方面的改变，我们获得的结果也会因此得到改善和提高。随着结果的改善，我们享受的成功的数量和质量也将增加。</p>
          <p>所以，<b>态度式激励</b>就是通过改变态度、调整和建立新的思维的习惯，不断获得我们想要获得的成果，而达成的激励。</p>

          <p>请仔细思考一下这个理念。想想如何能将刚刚所说的变为可能，以此大大提升你的成功。</p>
          <p>这就是我们将在这个项目中要做的事情。它不是一蹴而就的，但，一定会实现！</p>
          <p>请记住：你也不是一夜之间就成为现在的样子，所以保持耐心，接受这个过程的发生。</p>
          <p>&nbsp;</p>
          <p class="materials-title-s">4. 制约</p>
          <p>刚刚提到了制约的重要性，现在再来详细谈谈。我们的大部分制约是正向和富有成效的，但并非所有的制约都如此。</p>
          <p>孩童时期，“不”是我们最常听到的一个字。父母亲出于善意，怕我们受伤或担心我们打破东西，对我们频频说“不”。</p>
          <p>久而久之，“不”这个字便深植在你我内心，于是有些时候我们变得谨小慎微，优柔寡断。这有可能阻挠我们以更正向乐观的心态去探索生活，它使我们不是更多关注事情可以如何达成，反而是忙着替为何不能达成去找借口。</p>
          <p>下面这个例子可以用来解释制约是如何运作的：</p>
          <p>一头大象可以用象鼻轻而易举的挑起一吨重的货物，但在杂技团为什么这些被拴在很小桩子上的庞然大物却只是安安静静的呆着？</p>
          <p>这是因为驯兽员在这些象年幼弱小时，把它们用沉重的铁链拴在牢固的铁桩上。不管它们怎么努力，都无法挣脱铁链或挪动铁桩。于是，无论这些象长大后多么强壮，它们看到地上的桩子时就会想当然的认为它们挪不动，因此也不会再去努力挣脱。</p>
          <p>我们许多的聪明人有时就像马戏团的大象。他们的思想、行为和成果都被束缚，走不出自我强加的限制。捆住他们的唯一锁链只是他们自己较低的自我认知。</p>
          <p>本辅导项目就是帮助你挣断锁链、拔出桩子、打破自我限制，帮助你发挥出更大的潜能。</p>
          <p>&nbsp;</p>
          <p class="materials-title-s">5. 成功人士的三个特质</p>
          <p>研究发现成功人士都具备三个共同的特质：<b>积极心态、目标导向、自我激励。</b></p>
          <p><b>积极心态</b>：他们寻求事情可以如何达成，而非为何不能达成。他们关注自己的能力而非问题，关注自己的优势而非局限。</p>
          <p><b>目标导向</b>：他们知道自己现在在哪里，要往哪里去，以及如何才能到达。</p>
          <p><b>自我激励</b>：他们具备给自己上发条的能力，都是自我发动者。他们懂得在你能够理解、激励和领导别人之前，你必须首先理解、激励和领导你自己。</p>
          <p>参加完本项目之后，你将强化这些成功特质，发展出更积极正向的态度，更强的目标导向性，并且在发展略胜一筹的过程中更加动力十足的去达成你自己的目标。</p>
          <p>&nbsp;</p>
          <p class="materials-title-s">6. 略胜一筹</p>
          <p>人们普遍认为‘普通人’和‘成功人士’有天壤之别，但事实上两者之间的差距并不大。我们称此为“略胜一筹”。</p>
          <p>本辅导项目所做的就是帮你发展自己的“略胜一筹”。也许它只是一个很小的事情，如发现和打破一个固有的思维习惯、提前做个规划、思考何为自己的高回报活动……等等，但如果每日或每周发生了一点点对自己更有效的改变，那你将会怎样？</p>
          <p>&nbsp;</p>
          <p class="materials-title-s">7. 改变的循环</p>
          <p>我们知道如果总是以同样的方式思考，用同样的方式做事，我们得到的也只是同样的结果。所以为得到不同的结果，我们需要改变。但我们具体需要改变什么呢？</p>
          <p class="p-a">● 首先，为了得到不同的结果，我们需要清晰明确“我想要什么？”。请从自我的角色身份出发，思考自己想要达成的更佳状态或思考未来1-2年的发展方向，这些都有利于明确出你真正想要获得的结果。</p>
          <p class="p-a">● 评估“我现在的水平？”找到“我与更佳表现的差距？”，可以使你更加清楚起点以及要面对的真正挑战。本项目中提供的“自我评估表”和“我的挑战表”，希望能帮助你加强对自我挑战的觉察，因为没有觉察就无法改变。</p>
          <p class="p-a">● 为应对发现的这些挑战，我们需要做出改变，“为改变设定目标”帮助你达成真正想要获得的结果。</p>
          <p class="p-a">什么是目标？目标是用来衡量你达成目的的程度，因此在陈述目标时需要表明目的，而不是只给出一个指标。写下清晰明确的S.M.A.R.T.目标会给你带来达成目标的动力，这远比在头脑中想很多要更加重要。</p>
          <p class="p-a">● 没有“制定行动计划”的目标犹如天上漂浮的云，看得见却摸不着，久而久之美好的目标终成幻想，不但浪费了你宝贵的精力和资源，还有可能严重打击到自我的信心，感觉自己一事无成。所以如何“制定行动计划”，让我们能保持动力、善用资源、规避风险，筹划出务实有效的行动方法，也是需要培养和建立的思维习惯。</p>
          <p class="p-a">● 最后，行动并“评估已取得的进步”，不但能确保我们专注在正确的目标方向上，同时也能让你从不断取得的成果中学习并获得成就感，从而更有信心和动力继续追求你想要实现的。</p>

